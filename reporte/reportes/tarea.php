_Religión de la Cultura China

No existe una religión oficial en la cultura china, sino varias. Las principales son el budismo, el confucianismo y el taoísmo, aunque muchos las tratan de filosofías o pensamientos.

Estas tres “religiones” son incluyentes, es decir, que no se contradicen las unas a las otras, al contrario que las religiones abrahamicas (cristianismo, judaísmo e islam)De hecho, cada dinastía y emperador elegía su religión personal al gobernar, pero la de la corte y la del pueblo podía ser distinta a la del gobernante. Esto permitió un gran sincretismo religioso en China que se sigue manteniendo hoy en día.

La época de mayor mezcla de religiones fue durante la dinastía Yuan, cuando la apertura de la ruta de la seda permitió al cristianismo y al islam entrar en China.

_Mitología China

La mitología china es muy extensa. De hecho, no existe un único mito de la creación, sino varios. Uno cuenta que Fuxi y su esposa Nuwa, seres mitológicos mitad humano, mitad serpiente, crearon el mundo.

Fuxi le entrego a los hombres un código de leyes (parecido al de Hammurabi en Sumeria). Nuwa por el contrario creó a la humanidad al séptimo día a partir del barro. Estos dos seres significan la dualidad del universo, algo que después cogería el taoísmo para crear el Yin y el Yang. Otros seres mitológicos chinos son el dragon chino y el fénix.

_Escritura de la Cultura China

La escritura china se originó en la dinastía Xia a través de la escapulimancia. En su origen eran símbolos que los chamanes escribían en los caparazones de tortugas después de agrietarlos en el fuego. Una leyenda dice que fue creada por un ministro del Emperador Amarillo.

La escritura china actual son pictogramas e ideogramas compuestos por más 50.000 caracteres en total, aunque solo se usan unos 8.000 actualmente. Pues durante la época de Mao, el gobierno consiguió reducir el uso del numero de caracteres chinos con gran éxito.

_Caligrafía China

La caligrafía china es el arte de escribir los caracteres chinos con gran belleza, como si se tratase de una pintura. En la caligrafía de china, se mezcla la pintura, la escritura, la poesía y hasta la religion (sobretodo el taoísmo).

Es una forma de entender la cultura china en sí misma, y se necesitan años o incluso décadas de práctica para alcanzar a la perfección cualquiera de los cinco estilos históricos. Actualmente la caligrafía china es Patrimonio Cultural Inmaterial de la UNESCO.

_Idiomas en China

La gran extensión territorial de China crea la situación de que en el país haya unos 56 grupos étnicos distintos y varios grupos lingüísticos. Debido a esto, las lenguas de China ascienden al increíble número de 292. Aunque el idioma oficial del país es el chino mandarín estándar, que es el más usado por la población.

A pesar de que en China se hablan múltiples dialectos e idiomas distintos, la mayoría de las etnias y culturas usan el mismo tipo de escritura, algo que es único en el mundo.

Es decir, usan los mismos caracteres chinos para escribir cualquier concepto, idea, o cosa, aunque se pronuncie y diga distinto en la provincia vecina. Esto les permite comunicarse más fácilmente aunque no hablen el mismo idioma gracias a la escritura china.

_Costumbres y Tradiciones 

Existen muchos festivales y tradiciones en la cultura china. Uno de los más importantes es el Año Nuevo Lunar, o Festival de la Primavera. Este se celebra entre Enero y Febrero (el primer día del primer mes lunar) y dura unas dos semanas. La fiesta se caracteriza por los farolillos rojos, desfiles de dragones y fuegos artificiales.
Otra fiesta popular sería el Festival de Qingming o festival de los muertos chinos. Durante esta fiesta se visitan los cementerios, se realizan ofrendas de alimentos y se reza por los familiares fallecidos.

Otra de las mas famosas es el Festival del Barco Dragón, donde se hacen competiciones y carreras de botes con forma de dragones en los ríos.

_Gastronomía China

La gastronomía china suele usar muchos cereales como el arroz y el mijo. Los alimentos se dividen en Yin (frutas y verduras) y Yang ( fritos, asados, con especias, carnes, etc…).

En las comidas chinas se busca el equilibrio entre los cinco sabores básicos, es decir, el dulce, el salado, el ácido, el amargo y el picante. También suelen usar muchos tipos de especias y salsas. El té chino es la bebida más importante y consumida del país.

Tradicionalmente la comida china se come con mucha gente alrededor de una gran mesa redonda. La cortesía es muy importante. Por ejemplo, no puedes servirte a ti mismo las bebidas, sino que debes dejar que otros te la sirvan y servir tú a los demás.

Los licores y el alcohol se suelen tomar solo en celebraciones y fiestas. Y por supuesto, toda comida china se consume usando palillos.

_Vestimenta de la Cultura China

La vestimenta tradicional de la cultura china más importante es el hanfu. Este lo usan tanto hombres como mujeres, aunque varían según el genero. Los hanfus han ido cambiando con el paso de los siglos y las dinastías, haciéndose cada vez más ligeros y refinados.

Aunque hoy en día los trajes tradicionales solo se usan en ceremonias y fiestas nacionales, ya que la mayoría de la población viste igual que en Occidente.

El hanfu originario se componía de una túnica y una falda estrecha. Estos eran de varios colores, teniendo cada color un significado distinto. Una de las curiosidades de la cultura china es que el color amarillo solo podía ser usado por el emperador (en referencia al Emperador Amarillo).

Mientras que el rojo se usaba en fiestas, el purpura por los nietos del emperador y las mujeres jóvenes, el negro solo por los ancianos, y el blanco se llevaba únicamente de luto.

Aunque durante la última dinastía china, la Qing, se obligo a la población a vestir el qipao. Este era un traje parecido al hanfu, pero más estrecho, y con cordones en las mangas y en el cuello. Normalmente era de seda, aunque también se hacían de otros materiales.

_Arte de la Cultura China

La cultura de china es una de las que más arte ha dado a la humanidad. Ya sea en forma de pintura, poesía, música, opera, bailes, arquitectura, etc…

Actualmente China es el segundo país con más Patrimonios de la Humanidad declarados por la UNESCO, solo superado por Italia. Aunque todos los analisis indican que será el primero en un par de años.

Pintura de la Cultura China
La pintura china se inició en la cerámica y en las pinturas rupestres, pero más tarde se empezó a pintar sobre seda en la época de los “Reinos Combatientes” (siglo V a.C.).

Los colores más usados eran el rojo y el verde, lo que hizo que las pinturas chinas pasasen a conocerse con el nombre de Dan Qing.

En estas pinturas se representabas flores, pájaros, paisajes, animales, retratos y escenas cortesanas. Con la llegada del budismo a china se inicio la pintura al fresco. Esta trataba de temas religiosos. Son muy famosos los murales de las cuevas de Mogao.

_Música y Bailes de la Cultura China

Mitologicamente la música china fue creada por Linglun. Este ser mitológico también fue el descubridor de los tonos musicales. Históricamente, el origen de la música china proviene de otras zonas de Asia Oriental, aunque no se sabe con certeza.

El tipo de música más usada en China en la antigüedad es la heterofonica, proveniente de la etnia han. La heterofonia consiste en realizar variaciones a partir de una línea melódica.

La Opera de Pekín es la más famosa de toda China. Los cantos son de un tono muy alto y mantenido, acompañados de instrumentos de cuerda y percusión. Se caracteriza por ser una música muy rítmica y repetitiva.

Con respecto a los bailes y danzas chinas, estas se cuentan por millares según la ciudad y la región. Dos de las más importantes son la danza del dragón y la danza del león.

_Arquitectura de la Cultura China

China ha sido una de las grandes civilizaciones constructoras del mundo, y lo sigue siendo hoy día. De hecho, China posee una de las siete maravillas del mundo, la Gran Muralla China.

Esta empezó a construirse en el siglo V a.C. aunque sufriría varias reconstrucciones y ampliaciones durante toda la historia de la nación. La muralla no solo servía como defensa contra los pueblos nómadas del norte, sino también como vía de comunicación Este-Oeste.

Los chinos también ha construido otras edificaciones arquitectónicas como las pagodas, templos budistas, palacios imperiales, jardines chinos, ciudades y barrios, canales, etc…

Destaca sobretodo el Gran Canal, el canal más largo del mundo construido para conectar los ríos Yangtse y Huanghe a más de 2.000 kilómetros de distancia.

