_China:

La cultura china es una de las más complejas y antiguas del mundo (remontándose más de 5.000 años) ya que posee una gran cantidad de costumbres y tradiciones en una gran area geográfica, no solo dentro del país, sino también fuera de sus fronteras, por toda Asia Oriental.

_Ubicación y Origen de la Cultura China:

China significa en su propio idioma “País del Centro” o “País en el Centro del Mundo“. Actualmente la población china es de 1.500 millones de habitantes, siendo el país más poblado del mundo. El origen de la cultura china empezó en 2100 a.C. en la actual provincia de Henan, en el centro del país.

Aquí surgieron las culturas de Anyang y Erlitou, dos de las primeras culturas de China. Más tarde también surgiría aquí la primera dinastía china, la dinastía Xia.

Pero según la mitología China, su cultura comenzó hace 5.000 años con el Emperador Amarillo o Huang Di, uno de los Cinco Augustos o fundadores de China.

Este emperador mitológico sería el fundador de la dinastía Xia. Como iréis viendo más adelante, la mitología y la historia en China se llegan a mezclar muchas veces.

_Historia de la Cultura China

Si algo caracteriza la historia de la cultura oriental china con las demás es que es la única que posee una historia registrada de más de 5.000 años. La historia de China ha sido registrada en miles de documentos, analisis, libros, reliquias, registros nacionales, etc…

El historiador más famoso de China fue Sima Qian, el cual registró toda la historia de China desde sus orígenes hasta el siglo I a.C.

Muchos aspectos de la cultura del “Reino del Centro” fueron creados por emperadores mitológicos y sus esposas según la tradición.

Por ejemplo, el emperador Cangjie creó la escritura china, Shennong inventó la agricultura, Suiren enseñó a los humanos a usar el fuego, la emperatriz Lei Zu inventó la seda, etc… 

_Cultura China Antigua:

La Época Antigua fue la que más influyó en la cultura china de todas. En este periodo destacan la creación de la escapulimancia, y objetos de bronce y jade hallados en los enterramientos de algunos nobles y reyes de dinastías antiguas.

En ellos también se hacían sacrificios humanos y animales (caballos y perros). Uno de los más famosos es la tumba del primer emperador histórico de China Qi Shi Huang Di, en la cual se encuentran los famosos soldados de terracota.

